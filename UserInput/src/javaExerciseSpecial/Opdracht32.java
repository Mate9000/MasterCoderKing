package javaExerciseSpecial;

import java.util.Scanner;

public class Opdracht32 {
	private static Scanner input = new Scanner(System.in);
	public static void main(String[] args) {
		
		
		int i = input.nextInt();
		int x = input.nextInt();
		
		if (i != x);{
		System.out.println(i+"!="+x);
		}
		if (i < x);{
		System.out.println(i+"<"+x);
		}
		if (i <= x);{
		System.out.println(i+"<="+x);
		}
	}

}
